def addition(a: int or float, b: int or float) -> int or float:
    return a + b


def subtraction(a: int or float, b: int or float) -> int or float:
    return a - b


def multiplication(a: int or float, b: int or float) -> int or float:
    return a * b


def division(a: int or float, b: int or float) -> int or float:
    return a / b
